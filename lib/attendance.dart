class ActivityInfo {
  final String date;
  final String time;
  final String name;
  final String phone;
  final String email;
  final String sex;
  final String sector;
  final String purpose;

  ActivityInfo({
    required this.date,
    required this.time,
    required this.name,
    required this.phone,
    required this.email,
    required this.sex,
    required this.sector,
    required this.purpose,
  });
}
